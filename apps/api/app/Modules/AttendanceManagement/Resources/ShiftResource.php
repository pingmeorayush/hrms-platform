<?php

namespace App\Modules\AttendanceManagement\Resources;

use Illuminate\Http\Request;
use Illuminate\Http\Resources\Json\JsonResource;

class ShiftResource extends JsonResource
{
    public function toArray(Request $request): array
    {
        return [
            'id' => $this->id,
            'code' => $this->code,
            'name' => $this->name,
            'description' => $this->description,
            'start_time' => $this->start_time,
            'end_time' => $this->end_time,
            'break_duration_minutes' => $this->break_duration_minutes,
            'grace_minutes' => $this->grace_minutes,
            'working_hours_minutes' => $this->working_hours_minutes,
            'is_overnight' => $this->is_overnight,
            'status' => $this->status,
            'created_at' => $this->created_at?->toIso8601String(),
            'updated_at' => $this->updated_at?->toIso8601String(),
        ];
    }
}
