export function buildDemoPerformanceWorkspace(snapshot: AccessSnapshot | null): PerformanceWorkspaceData {
  const permissions = snapshot?.user.permissions ?? []
  const linkedEmployeeId = snapshot?.user.employee?.id ?? null
  const canManage = permissions.includes('performance.manage')
  const canReview = permissions.includes('performance.review') || canManage
  const canCalibrate = permissions.includes('performance.calibrate') || canManage

  const visibleGoals = canManage || canReview
    ? goals
    : goals.filter((goal) => goal.owner_employee?.id === linkedEmployeeId)

  const visibleReviews = reviews.filter((review) => {
    if (canManage || canCalibrate) {
      return true
    }

    if (linkedEmployeeId && review.employee?.id === linkedEmployeeId) {
      return true
    }

    if (linkedEmployeeId && review.manager_employee?.id === linkedEmployeeId) {
      return true
    }

    return review.reviewer_user_ids.includes(snapshot?.user.id ?? -1)
  })

  return {
    goals: visibleGoals.map((goal) => ({ ...goal })),
    competencies: competencies.map((competency) => ({ ...competency })),
    reviewCycles: reviewCycles.map((cycle) => ({ ...cycle })),
    reviews: visibleReviews.map((review) => ({ ...review })),
    employees: employeeDirectory.map((employee) => ({ ...employee })),
    meta: {
      can_manage: canManage,
      can_review: canReview,
      can_calibrate: canCalibrate,
      linked_employee_id: linkedEmployeeId,
    },
  }
}
